class EditorData {
  dateEvent(int data) {
    if (data == 1) {
      return 'JANEIRO';
    } else if (data == 2) {
      return 'FEVEREIRO';
    } else if (data == 3) {
      return 'MARÇO';
    } else if (data == 4) {
      return 'ABRIL';
    } else if (data == 5) {
      return 'MAIL';
    } else if (data == 6) {
      return 'JUNHO';
    } else if (data == 7) {
      return 'JULHO';
    } else if (data == 8) {
      return 'AGOSTO';
    } else if (data == 9) {
      return 'SETEMBRO';
    } else if (data == 10) {
      return 'OUTRUBRO';
    } else if (data == 11) {
      return 'NOVEMBRO';
    } else if (data == 12) {
      return 'DEZEMBRO';
    } else {
      return '';
    }
  }

